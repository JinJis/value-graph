"""PH-PROV3 query-time evidence highlighter — PyMuPDF, no browser.

Given a cached filing PDF (`EvidenceDoc`) and the figure the answer cited, find that value in
the PDF text (tried at the unit scales statements use: 원/천원/백만원/억원 · ones/thousands/
millions), anchor it on its account label, highlight the cell, and rasterize the page band to
PNG. Deterministic text match (no LLM); cache-first so the highlight is computed once per
(filing, figure). This replaces the Chromium screenshot in the query hot-path.
"""

from __future__ import annotations

import hashlib
import pathlib

import fitz  # pymupdf

from app.config import settings
from app.providers.kr.dart_document import KR_LABELS

RENDER_VERSION = "1"
_SCALES = (1, 1_000, 1_000_000, 100_000_000)
_Y_TOL = 6.0    # pts — value cell on the same visual line as its label
_BAND_PAD = 44.0  # pts of vertical context above/below the highlighted row
_ZOOM = 2.0

# us-gaap concept → the English statement labels it renders as (for anchoring the value).
# Mirrors agent-engine evidence._FIELD_CONCEPTS; only the headline lines need anchoring.
US_GAAP_LABELS: dict[str, list[str]] = {
    "Revenues": ["Net sales", "Total net sales", "Total revenue", "Revenue", "Revenues", "Net revenue"],
    "RevenueFromContractWithCustomerExcludingAssessedTax": ["Net sales", "Total net sales", "Revenue", "Total revenue"],
    "RevenueFromContractWithCustomerIncludingAssessedTax": ["Net sales", "Total net sales", "Revenue", "Total revenue"],
    "SalesRevenueNet": ["Net sales", "Total net sales", "Revenue"],
    "NetIncomeLoss": ["Net income", "Net income (loss)"],
    "ProfitLoss": ["Net income", "Profit", "Profit (loss)"],
    "OperatingIncomeLoss": ["Operating income", "Operating income (loss)"],
    "GrossProfit": ["Gross profit", "Gross margin"],
    "Assets": ["Total assets"],
    "Liabilities": ["Total liabilities"],
    "StockholdersEquity": ["Total stockholders’ equity", "Total shareholders’ equity", "Total equity"],
    "StockholdersEquityIncludingPortionAttributableToNoncontrollingInterest": ["Total equity"],
    "CashAndCashEquivalentsAtCarryingValue": ["Cash and cash equivalents"],
    "CashCashEquivalentsRestrictedCashAndRestrictedCashEquivalents": ["Cash and cash equivalents"],
    "NetCashProvidedByUsedInOperatingActivities": ["Cash generated by operating activities",
                                                   "Net cash provided by operating activities"],
    "NetCashProvidedByUsedInInvestingActivities": ["Cash used in investing activities",
                                                   "Net cash used in investing activities"],
    "NetCashProvidedByUsedInFinancingActivities": ["Cash used in financing activities",
                                                   "Net cash used in financing activities"],
}


def labels_for(market: str, concept: str) -> list[str]:
    """Candidate account labels to anchor the value on, from the /evidence concept param.
    KR concept = our field name → KR account labels; US concept = comma us-gaap candidates."""
    if (market or "").upper() == "KR":
        return KR_LABELS.get(concept, [])
    out: list[str] = []
    for tag in concept.split(","):
        out += US_GAAP_LABELS.get(tag.split(":")[-1].strip(), [])
    return list(dict.fromkeys(out))  # dedupe, keep order


def _candidates(value: float) -> list[str]:
    """The value formatted with thousands commas at each plausible unit scale."""
    out: list[str] = []
    for scale in _SCALES:
        q = abs(value) / scale
        if q >= 1 and abs(q - round(q)) < 0.5:
            s = f"{round(q):,}"
            if s not in out:
                out.append(s)
    return out


def _locate(doc, value: float, labels: list[str]) -> tuple[int, "fitz.Rect"] | None:
    """(page_index, value_rect) for the figure — anchored on a label when given. Prefers a
    value cell on the same line as a label; else the first occurrence (label-anchored only
    when labels exist, so arbitrary values still locate). None if not found."""
    cands = _candidates(value)
    if not cands:
        return None
    fallback: tuple[int, fitz.Rect] | None = None
    for pno in range(doc.page_count):
        page = doc[pno]
        label_rects = [r for lab in labels for r in (page.search_for(lab) or [])]
        for cand in cands:
            for vr in page.search_for(cand) or []:
                on_label_line = any(
                    abs((lr.y0 + lr.y1) / 2 - (vr.y0 + vr.y1) / 2) < _Y_TOL and lr.x1 <= vr.x0 + 5
                    for lr in label_rects
                )
                if on_label_line:
                    return pno, vr           # best: value next to its label → done
                if fallback is None and not labels:
                    fallback = (pno, vr)      # no labels → first occurrence is all we can claim
    return fallback


def _render(pdf_path: str, value: float, labels: list[str]) -> bytes | None:
    try:
        doc = fitz.open(pdf_path)
    except Exception:  # noqa: BLE001 — missing/corrupt PDF → no image
        return None
    try:
        hit = _locate(doc, value, labels)
        if hit is None:
            return None
        pno, vr = hit
        page = doc[pno]
        page.add_highlight_annot(vr)  # amber highlight on the cited figure
        band = fitz.Rect(0, max(0, vr.y0 - _BAND_PAD), page.rect.width,
                         min(page.rect.height, vr.y1 + _BAND_PAD))
        return page.get_pixmap(matrix=fitz.Matrix(_ZOOM, _ZOOM), clip=band).tobytes("png")
    except Exception:  # noqa: BLE001
        return None
    finally:
        doc.close()


def highlight_png(pdf_path: str, value: float, labels: list[str]) -> bytes | None:
    """Cache-first highlighted PNG of ``value`` in the cached filing PDF (or None)."""
    key = hashlib.sha256(
        f"{RENDER_VERSION}|{pdf_path}|{value}|{','.join(labels)}".encode()).hexdigest()[:32]
    cache = pathlib.Path(settings.evidence_docs_dir) / "cache" / f"{key}.png"
    if cache.exists():
        return cache.read_bytes()
    png = _render(pdf_path, value, labels)
    if png is None:
        return None
    cache.parent.mkdir(parents=True, exist_ok=True)
    cache.write_bytes(png)
    return png
